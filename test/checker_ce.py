def test_ce(json_file):
    struct = build_struct('real_topo/autoencoder_result.txt')
    tree = build_tree(struct, 0)
    post_seq = post_order(tree)
    for i, node in enumerate(post_seq):
        if locations[i] == 1:
            node.dpr = True
        else:
            node.dpr = False

    pre_seq = pre_order_without_leaf(tree)
    print(pre_seq)

    anomaly_matrix = get_anomaly_matrix(json_file)
    total, avg = get_average_hosts_to_check(pre_seq, anomaly_matrix)
    print(total, avg)
    return total, avg


if __name__ == '__main__':
    result = {
        'total_ce': [],
        'avg_ce': []
    }
    for day in range(30):
        input_file = f'real_topo/all_anomalies_{day}.json'
        total_ce, avg_ce = test_ce(input_file)
        result['total_ce'].append(total_ce)
        result['avg_ce'].append(avg_ce)
    df = pd.DataFrame(result)
    df.to_csv('../result/result_ce_all.csv')
